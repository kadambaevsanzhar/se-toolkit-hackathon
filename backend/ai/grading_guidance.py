"""Shared grading guidance for analyzer and validator across school subjects."""

GENERAL_GRADING_GUIDANCE = """
Global grading dimensions:
- correctness relative to the visible task
- completeness of the answer
- reasoning, justification, or evidence
- clarity and structure
- subject-appropriate terminology and method

Subject-specific guidance:
- Mathematics / Physics / Chemistry calculations:
  correctness of formulas, transformations, intermediate steps, and final answer dominates
- History / Literature / Social science:
  judge relevance, factual accuracy, argument quality, evidence/examples, and completeness
  identify the first materially false, unsupported, or irrelevant claim instead of blaming a correct earlier sentence
- English / Language learning:
  judge task completion, meaning, grammar/spelling when relevant, clarity, and vocabulary appropriateness
  identify the first decisive meaning, grammar, usage, or task-completion problem when one exists
- Biology / Geography / Economics / Computer science conceptual work:
  judge conceptual correctness, completeness, relevance, and explanation quality
  identify the first materially incorrect concept, claim, or inference when one exists

General rule:
- Do not apply math-only logic to every subject.
- Find the first real error for the actual subject.
- Short but readable subject-specific answers can still be valid academic submissions.
- If the answer is strong, explain why in subject-appropriate language.
- If the answer is weak, explain the concrete content problem, not just that it is "wrong".
""".strip()
